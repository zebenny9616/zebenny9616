def add(n1, n2):
    return(n1+n2)
def sub(n1, n2):
    return(n1-n2)
def mult(n1, n2):
    return(n1*n2)
def div(n1, n2):
    return(n1/n2)

n1 = int(input('Entrez le premier chiffre: '))
n2 = int(input('Entrez le second chiffre: '))

print(add(n1, n2))
print(sub(n1, n2))
print(mult(n1, n2))
print(div(n1, n2))